package es.ieeesb.nevera.utils;

/**
 * The Class User.
 */
public class User {

	/** The id. */
	public int id;
	
	/** The name. */
	public String name;
	
	/** The credit. */
	public double credit;
	
	/**
	 * Instantiates a new user.
	 *
	 * @param id the id
	 * @param name the name
	 * @param credit the credit
	 */
	public User(int id, String name, double credit) {
		this.id = id;
		this.name = name;
		this.credit = credit;
	}

}
